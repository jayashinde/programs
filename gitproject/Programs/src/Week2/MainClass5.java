public class MainClass5 {
    public static void main(String[] args) {
        // Declaration
        String welcomeMessage;
//Initialisation
        welcomeMessage = "Good Morning";
        System.out.println(welcomeMessage);
//Assigning
        welcomeMessage = "Good Afternoon";
        System.out.println(welcomeMessage);
//Re-Assigning
        welcomeMessage = "Good Evening";
        System.out.println(welcomeMessage);
    }
}
